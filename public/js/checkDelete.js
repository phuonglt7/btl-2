function ConfirmDelete()
{
    var x = confirm("BẠn có muốn thực hiện xóa?");
    if (x)
        return true;
    else
      	return false;
}

function ConfirmTransfor()
{
    var transfor = confirm("Bạn có muốn thực hiện chuyển tiền?");
    if (transfor)
        return true;
    else
      	return false;
}